const mongoose = require("mongoose");

const userSchema = new mongoose.Schema(
  {
    firstName: {
      type: String,
      required: true,
      minLength: 4,
      maxLength: 50,
    },
    lastName: {
      type: String,
      minLength: 4,
      maxLength: 50,
    },
    emailId: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true,
    },
    password: {
      type: String,
      required: true,
      unique: true,
    },
    age: {
      type: Number,
      min: 18,
    },
    gender: {
      type: String,
      validate(value) {
        if (!["male", "female", "others"].includes(value)) {
          throw new Error("Gender is not valid");
        }
      },
    },
    pgotoUrl: {
      type: String,
      default:
        "https://tse4.mm.bing.net/th?id=OIP.3Mdl0Zq5aYdiRFzd5CXIuwHaHT&pid=Api&P=0&h=220",
    },
    about: {
      type: String,
      default: "This is a default about of the user!",
    },
    skills: {
      type: [String],
    },
  },
  {
    timestamps: true,
  }
);

// const User = mongoose.model("User", userSchema);
// Whenever you referencing to a model the first latter must be capital like (User).

module.exports = mongoose.model("User", userSchema);
